# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# So existe duas respostas possiveis
# Sim(True) ou nao(False).
# Existem varios operadores para "qustionar".
# Dentre eles o ==, que e um operador logico que questiona
# se um valor e igual ao outro

print(10 == 10) # Sim => True(Verdadeiro)
print(type(10 == 11)) # Nao => False(Falso)